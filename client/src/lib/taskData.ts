export function getRuleForTask(task: Task): Rule | undefined {
  if (task.ruleId) return goldenRules.find(r => r.id === task.ruleId);
  const categoryRules = goldenRules.filter(r => r.category === task.category);
  return categoryRules.length > 0 ? categoryRules[0] : undefined;
}

export function getBuddyHint(ruleId: number, methodology?: Methodology): string {
  const method = methodology || USER_METHODOLOGY;
  const rule = goldenRules.find(r => r.id === ruleId);
  if (!rule) return "";
  return rule.hints[method] || rule.hints.default;
}

