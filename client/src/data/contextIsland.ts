const CONTEXT_HINT =
  "Прочитай предложение ещё раз. Какое слово помогает понять смысл? Подумай: что значит это слово именно здесь, в этом предложении?";

function contextTask(
  id: number,
  sentence: string,
  targetWord: string,
  modelAnswer: string,
  acceptableAnswers: string[],
  unacceptablePatterns: string[],
  difficulty: "базовый" | "повышенный"
): Task {
  const difficultyNum = difficulty === "базовый" ? 1 : 2;
  const keywords = modelAnswer
    .toLowerCase()
    .replace(/[.,!?;:«»""—–\-()]/g, "")
    .split(/\s+/)
    .filter((w) => w.length >= 2)
    .slice(0, 5);
  return {
    id,
    type: "context",
    word: targetWord,
    question: `Объясни, что значит это слово в предложении.\n\n«${sentence}»\n\nСлово: ${targetWord}`,
    correct: modelAnswer,
    options: [],
    audio: modelAnswer,
    hint: CONTEXT_HINT,
    rule: null,
    ruleId: 20,
    difficulty: difficultyNum,
    category: "context",
    inputType: "text",
    acceptableAnswers,
    unacceptablePatterns,
    keywords: keywords.length >= 2 ? keywords : [modelAnswer.slice(0, 15)],
  };
}

