function updateRuleId() {
  let content = fs.readFileSync(TASK_FILE, "utf-8");
  let updatedCount = 0;

  PHONETICS_TASK_IDS.forEach((id) => {
    // Находим задание по id и заменяем только ruleId: 2 на ruleId: 23
    const regex = new RegExp(
      `(id:\\s*${id}[\\s\\S]{0,800}?)(ruleId:\\s*)2\\b`,
      "m"
    );
    if (regex.test(content)) {
      content = content.replace(regex, "$1$223");
      updatedCount++;
      console.log(`✅ Task #${id}: ruleId updated to 23`);
    }
  });

  fs.writeFileSync(TASK_FILE, content, "utf-8");
  console.log(`\n🎉 Готово! Обновлено ${updatedCount} заданий.`);
}

updateRuleId();
